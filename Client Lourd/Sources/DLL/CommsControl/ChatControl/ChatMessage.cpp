#include "ChatMessage.h"
#include <ctime>
#include <string>

////////////////////////////////////////////////////////////////////////
///
/// @fn ChatMessage::ChatMessage(std::string message)
///
/// @brief Construit un objet ChatMessage
///
/// @param[in]
/// @param[in]
///
/// @remarks CONSTRUCTEUR UTILISÉ PAR LE CLIENT UNIQUEMENT
///
/// @return aucun.
///
////////////////////////////////////////////////////////////////////////
ChatMessage::ChatMessage(std::string message)
{
	messageContent_ = message;
}

////////////////////////////////////////////////////////////////////////
///
/// @fn std::string ChatMessage::getMessage()
/// 
/// @brief Retourne le contenu du message
///
////////////////////////////////////////////////////////////////////////
std::string ChatMessage::getMessage()
{
	return messageContent_;
}